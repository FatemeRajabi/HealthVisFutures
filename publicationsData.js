function selectTabBasedOnExistingParam() {
    console.log("here");
    const urlParams = new URLSearchParams(window.location.search);
    const tab = urlParams.get('tab'); // Get the value of the 'tab' query parameter

    if (tab) {
        const tabButton = document.querySelector(`button[onclick*="${tab}"]`);
        if (tabButton) {
            // Create a dummy event to pass to the openData function
            const event = new Event('click');
            tabButton.dispatchEvent(event); // Trigger the click event for the corresponding button
        }
    } else {
        // Optionally, you can set a default tab if no query parameter is present
        document.querySelector(`button[onclick*="human"]`).click();
    }
}

